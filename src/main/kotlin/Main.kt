fun main() {
    val add = 10 + 12
    println(add) // 22

    val minus = 66 - 22
    println(minus) // 44

    val mul = 16 * 2
    println(mul) // 32

    val divide = 36 / 12
    println(divide) // 3

    val divide2 = 18 / 2
    println(divide2) // 9 (!)

    val divide3 = 28 / 7
    println(divide3) // 4

    val divide4 = 30 / 2.0
    println(divide4) // 15 (!)

    val remainder = 7 % 2
    println(remainder) // 1
}
